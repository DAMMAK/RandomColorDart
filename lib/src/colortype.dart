/// Defines the different types of colors that can be generated.
enum ColorType {
  /// Generates a completely random color.
  random,

  /// Generates a monochrome (grayscale) color.
  monochrome,

  /// Generates a shade of red.
  red,

  /// Generates a shade of orange.
  orange,

  /// Generates a shade of yellow.
  yellow,

  /// Generates a shade of green.
  green,

  /// Generates a shade of blue.
  blue,

  /// Generates a shade of purple.
  purple,

  /// Generates a shade of pink.
  pink,
}
